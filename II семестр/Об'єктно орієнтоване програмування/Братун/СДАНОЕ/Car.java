public class Car {

    public String name;
    public int price;
    public String color;
    public int mileage;
    public String country;


    public Car(String names, int prices, String colores, int mileages, String countryes) {
        name = names;
        price = prices;
        color = colores;
        mileage = mileages;
        country = countryes;
    }
}

