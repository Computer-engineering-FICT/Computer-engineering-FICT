x=float(input("Write x "))             #дозволяємо ввести змінні з клавіатури
y=float(input("Write y "))
if (x**2+y**2) > ((x+y)**2):           #задаємо умову за якої сума квадратів більша
    print("(x**2+y**2) > ((x+y)**2)")
elif (x**2+y**2) < ((x+y)**2):         #задаємо умову за якої сума квадратів менша
    print("(x**2+y**2) < ((x+y)**2)")
else:                                  #задаємо умову за якої сума квадратів та квадрат суми рівні
    print("(x**2+y**2) = ((x+y)**2)")
