"""Ввести з клавіатури дійсне число A і ціле число N > 0.Знайти A в степені N шляхом множення числа самого на себе
AN = A · A ·…· A (числа A перемножуються N разів). Вивести проміжні та кінцевий результат."""

print('Введите число А и его степень N')
A = float(input('A = '))
N = int(input('N = '))

for i in range(N+1):
    print(int(A**i))
