""" Ввести послідовність символів, що містить текст. Визначити довжину максимальної серії символів відмінних від букв,
яка входить в послідовність. """

import string

text = bytes(input('Введіть послідовність симвлолів:\n'), encoding='utf-8')
a = 0
res = 0

for i in text:
    if chr(i) not in string.ascii_letters:
        a += 1
        if a > res:
            res = a
    else:
        a = 0

print('Довжина максимальної серії символів, відмінних від букв:', '%3s' % res)
